c = 11;

console.log('\nWHILE\n')
// WHile apenas executa se for verdade

while (c <= 10) {
   console.log(`Número ${c}`);
   c ++;
}

// Do While sempre irá ser executado ao menos uma vez mesmo que seja falso
console.log('\nDO WHILE\n')

do {
   console.log(`Número ${c}`)
   c++
} while (c <= 10)

